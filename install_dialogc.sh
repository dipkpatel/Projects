#Build function, that build the necessary files required to generate
#the Dialogc Interface. 
build(){
	echo "Running Self Extracting Dialog Installer"
	echo "The Installation will create a new folder name Dialog"
	echo "in the current directory and extract its contents there."
	echo ""
	#Confirm while loop that checks whether the user wishes to 
	#continue with the build or not.
	while true; do
		read -p "Do you wish to install this program? " yes_no
		case $yes_no in
			[Yy]* )
			echo "Building the Interface"
			uudecode $0 > /dev/null 
			tar -zxvf Dialogc.tar.gz > /dev/null  
			rm -rf Dialogc.tar.gz
			cd Dialogc
			echo "Running Makefile"
			make > /dev/null
			break;;
		[Nn]* ) 
			echo "NO" 
			exit;;
		* ) 
			echo "Please answer yes (Y or y) or No (N or n).";
			;;
		esac
	done
	echo "Build Successful!"
}
#Install function, which asks the user for Directory and 
#installs the Dialogc program to that Dir.
install(){
	#Asking user for the Directory they wish to install in.
	echo "____________________________________________________________"
	echo "Please Enter the directory you wish to install the Software."
	echo ""
	echo "(You may provide, full path to the Directory, if you provide only a name of the folder,"
	echo "the script will try to look for that folder in current Directory.)"
	read data
	#If state to check whether the entered Directory exitst
	#and if the script has permission to write to it.

	if [ -d "$data" ]; then
		echo "DIR: $data exist ";
		#Dir exist, check for permission
		if [ -r "$install_dir" ]; then
			echo "You do have the permission to write to this DIR: $data"
		else
			echo "You do NOT have the permission to write to this DIR: $data"
			echo "The Installation Will Stop now!"
			exit 0
		fi
	else
		#if the entered Directory does not exist
		echo "The entered directory does not Exist."
		#ask for confirmation to create a new DIR 
		while true; do 
			read -p "Would you like to create this directory (Y/n)? " ans
			case $ans in
				[Yy]* )
					#yes option choosen
					echo "Creating DIR: $data"
					#creating the necessary Directory
					mkdir -p $data
					mkdir -p $data/bin/ImageIcon
					mkdir -p $data/lib
					echo "Required directory created."
					break;;
				[Nn]* ) 
					#no option chosen, Exit Installation
					echo "You choose not to create the directory, thus the script will not EXIT." 
					exit;;
				* ) 
					echo "Please answer yes (Y or y) or No (N or n).";
					;;
			esac
		done
	fi
	clear
	#Asking and confirming the change to the .bashrc file for the Library path modification
	echo "The Export PATH for the .bashrc needs to be modify."
	echo ""
	#while loops to ask to confirmation of changing library path
	while true; do 
		read -p "Would you proceed with this modification (Y/n)? " ex
		case $ex in
			[Yy]* )
				echo "Changing the Export path.."
				echo 'export LD_LIBRARY_PATH='$(pwd)/$data/lib/':${LD_LIBRARY_PATH}' >> $HOME/.bashrc 
				echo "Pleae source or reopen the bash shell to run the Dialogc IDE"
				echo "The export path change."
				break;;
				[Nn]* ) 
				echo "You choose not to export path, thus the script will not EXIT." 
				exit;;
			* ) 
				echo "Please answer yes (Y or y) or No (N or n).";
				;;
			esac
	done

	#copying all the relavant files needed to operate Dialogc interface
	cp Dialogc/*.class $data/bin/
	cp Dialogc/*.o $data/bin/
	cp Dialogc/yadc $data/bin/
	cp Dialogc/ImageIcon/*.gif $data/bin/ImageIcon/
	cp Dialogc/libJNIpm.so $data/lib/
	echo "All the necessary Files are copied in the Directory: $data/bin and $data/lib folders"
	#Cleaning up the building Files
	rm -rf Dialogc/
	echo "Installation Completed."
}

#if condiftion to check the command ling arguments 
if [ "$1" == "--build" ]; then
	#builds the interface and exits
	build
	exit
elif [ "$1" == "--install" ]; then 
	#install the interface and exit
	install
	exit
else
	#build the interface and install it
	build
	cd ..
	install
	exit
fi
#end of script
exit

